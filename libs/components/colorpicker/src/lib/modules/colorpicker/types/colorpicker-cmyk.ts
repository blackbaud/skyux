/**
 * Colors specified as a combination of cyan, magenta, yellow, and black.
 */
export interface SkyColorpickerCmyk {
  /**
   * The percentage of cyan.
   */
  cyan: number;
  /**
   * The percentage of magenta.
   */
  magenta: number;
  /**
   * The percentage of yellow.
   */
  yellow: number;
  /**
   * The percentage of black.
   */
  key: number;
}
